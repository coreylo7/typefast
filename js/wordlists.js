export function getRandomWordByCategory(category, lengthRange = null) {
  const words = WORD_LISTS[category] || WORD_LISTS.common;
  let filtered = lengthRange
    ? words.filter(w => w.length >= lengthRange.min && w.length <= lengthRange.max)
    : words;
  if (filtered.length === 0) filtered = words;
  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function getWeightedCategory(weights) {
  const rand = Math.random();
  let cumulative = 0;
  for (const [cat, weight] of Object.entries(weights)) {
    cumulative += weight;
    if (rand < cumulative) return cat;
  }
  return 'common';
}
